#Arithmetic operators
x = 5
y = 10
print(x + y)
print (x - y)
print(x * y)
print(x % y)
print(x // y)
print(x / y)
print(x**y)

#Assignment operators
x = 15
x+=5
print(x)
x-=4
print(x)
x*= 2
print(x)
x = 40
x/=3
print(x)
x%=5
print(x)
x//=25
print(x)
x**=2
print(x)

#Relational operators
print(12 == 12)
print(13 < 12)
print(13 > 12)
print(15 <= 35)
print(20 >= 30)
print(10 < x < 20)

#and, or, not operators
print(12 < 5 and 13 < 15)
print(12 < 15 or 13 > 15)
print(not(12 < 15 or 13 > 15))

#Bitwise operators
x = 6
y = 3
print(x & y)
print(x | y)
print(x^y)
print(~x)
print(x << 2)
print(x >> 2)
